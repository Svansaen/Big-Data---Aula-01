#Programa igual ou diferente 
n1 = int(input("Informe o valor do primeiro número: "))
n2 = int(input("Informe o valor do segundo número: "))
if n1 == n2 :
    #produto = n1 * n2
    print(f"O valor da multiplicação é : {n1 * n2}")
else:
    #soma = n1 + n2
    print(f"O valor da soma é: {n1 + n2}")   